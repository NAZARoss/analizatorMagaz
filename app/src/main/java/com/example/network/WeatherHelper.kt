    fun getWeatherDescription(code: Int): String {
        return when (code) {
            0 -> "Ясно"
            1 -> "Преимущественно ясно"
            2 -> "Переменная облачность"
            3 -> "Пасмурно"
            45, 48 -> "Туман"
            51, 53, 55 -> "Моросящий дождь"
            56, 57 -> "Ледяная морось"
            61 -> "Небольшой дождь"
            63 -> "Умеренный дождь"
            65 -> "Сильный дождь"
            66, 67 -> "Ледяной дождь"
            71 -> "Небольшой снегопад"
            73 -> "Умеренный снегопад"
            75 -> "Сильный снегопад"
            77 -> "Снежная крупа"
            80, 81, 82 -> "Ливневый дождь"
            85, 86 -> "Снегопад"
            95 -> "Гроза"
            96, 99 -> "Гроза с градом"
            else -> "Переменные условия"
        }
    }

    fun getWeatherEmoji(code: Int): String {
        return when (code) {
            0 -> "☀️"
            1, 2 -> "🌤️"
            3 -> "☁️"
            45, 48 -> "🌫️"
            51, 53, 55, 56, 57 -> "🌧️"
            61, 63 -> "🌦️"
            65 -> "🌧️"
            66, 67 -> "🌨️"
            71, 73, 75, 77 -> "❄️"
            80, 81, 82 -> "🌧️"
            85, 86 -> "❄️"
            95, 96, 99 -> "🌩️"
            else -> "🌤️"
        }
    }

    fun isRainCode(code: Int): Boolean {
        return code in listOf(
            51, 53, 55, 56, 57,          // морось
            61, 63, 65, 66, 67,          // дождь
            71, 73, 75, 77,              // ❄️ снег
            80, 81, 82,                  // ливень
            85, 86,                      // ❄️ снегопад
            95, 96, 99                   // гроза
        )
    }
}
